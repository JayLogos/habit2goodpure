    private Long getMinimumLatency(String invokedTime) {
        Long latencyTime=0L;

        try {
            Calendar today = Calendar.getInstance();
            Calendar scheduledDay = Calendar.getInstance();

            int hour = today.get(Calendar.HOUR);
            int second = today.get(Calendar.SECOND) + hour*60;

            SimpleDateFormat sdf = new SimpleDateFormat("h:m", Locale.getDefault());
            Date tmpScheduledTime = sdf.parse(invokedTime);
            scheduledDay.setTime(tmpScheduledTime);

            int shour = scheduledDay.get(Calendar.HOUR);
            int ssecond = scheduledDay.get(Calendar.SECOND) + shour*60;

            int gap = second - ssecond;
            if (gap <= 0) {
                latencyTime =  (24*60*60 - gap) * 1000L;
            } else {
                latencyTime = gap * 1000L;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        return latencyTime;
    }
