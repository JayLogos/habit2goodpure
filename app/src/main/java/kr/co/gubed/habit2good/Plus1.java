package kr.co.gubed.habit2good;

public class Plus1 {
    String day;
    int count;

    public Plus1() {
    }

    public Plus1(String day, int count) {
        this.day = day;
        this.count = count;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
